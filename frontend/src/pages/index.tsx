import Head from "next/head";
import Link from "next/link";

import styles from "./index.module.css";
import { env } from "~/env";
import { Button, ButtonGroup, Container, Heading } from "@chakra-ui/react";
import { useRouter } from "next/router";
import axios from "axios";



export default function Home() {

  const router = useRouter()

  return (
    <>
      <Head>
        <title>Create T3 App</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Container  display={"flex"} flexDirection={"column"} alignItems={"center"}>
        <Heading>Mis ventas </Heading>
        <ButtonGroup>
          <Button
            colorPalette={"blue"}
            onClick={() => {
              router.push("/login")
            }}>Login
          </Button>

          <Button
            colorPalette={"green"}
            onClick={() => {
              // router.push("/sales")
              axios.get(`${env.NEXT_PUBLIC_BACKEND_BASE_URL}/sales/`, { withCredentials: true })
            }}>Get sales
          </Button>

          <Button
            colorPalette={"purple"}
            onClick={() => {
              // router.push("/sales")
              axios.post(`${env.NEXT_PUBLIC_BACKEND_BASE_URL}/sales/`, {
                operation_date: new Date(),
                total_amount: 1000
              },
                {
                  withCredentials: true
                })
            }}>Create Sale
          </Button>
        </ButtonGroup>
      </Container>
    </>
  );
}
